public class IndexPosition {
        public static void main(String args []){
            int x [] ={25,14,56,15,36,56,77,18,29,49};
            for(int i=0;i<x.length;i++){
            if(x[i]==25){
                System.out.println("Index position 25: " + i);
            }
            if(x[i]==77){
                System.out.println("Index position 77: " + i);
            }
            }
        }
}
